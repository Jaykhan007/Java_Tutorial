package Introduction;

public class Variables {
  
  public static void main(String[] args) {
	
	//hard coded / fix value variable
	int numOne = 30;
	int numTwo = 20;
	
	// Dynamic variables
	int sum = numOne + numTwo;
	
	System.out.println(sum);
	
	
	double a = 3.00;
	double b = 4.00;
	
	double c = Math.sqrt(a * a + b * b);
	System.out.println(c);
	
	
  }
  
  
}
